package net.firejack.platform.core.model.registry.directory;


import net.firejack.platform.core.model.registry.LookupModel;
import net.firejack.platform.core.model.registry.RegistryNodeModel;
import net.firejack.platform.core.model.registry.RegistryNodeType;


/*@Entity
@TreeLookup(RegistryNodeModel.class)
@Table(name = "opf_application")*/
public class ApplicationModel extends LookupModel<RegistryNodeModel> {

    private String consumerKey;
    private String consumerSecret;
    private String callbackUrl;

    public String getConsumerKey() {
        return consumerKey;
    }

    public void setConsumerKey(String consumerKey) {
        this.consumerKey = consumerKey;
    }

    public String getConsumerSecret() {
        return consumerSecret;
    }

    public void setConsumerSecret(String consumerSecret) {
        this.consumerSecret = consumerSecret;
    }

    public String getCallbackUrl() {
        return callbackUrl;
    }

    public void setCallbackUrl(String callbackUrl) {
        this.callbackUrl = callbackUrl;
    }

    @Override
    public RegistryNodeType getType() {
        return RegistryNodeType.APPLICATION;
    }

}
